package graph.detail;

public class Circle {
    private double x;
    private double y;
    private long r;

    public Circle(double x, double y, long r) {
        this.x = x;
        this.y = y;
        this.r = r;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public long getR() {
        return r;
    }
}
